class Student:
    def __init__(self, name, age, gender):
        self.name = name
        self.age = age
        self.gender = gender

# Create an instance of Student
student1 = Student("John", 20, "Male")

# Print the student's details
print(student1.name, student1.age, student1.gender)

student2 = Student("Sara", 30, "Female")

# Print the student's details
print(student2.name, student2.age, student2.gender)

student3 = Student("mark", 18, "Male")

# Print the student's details
print(student3.name, student3.age, student3.gender)
