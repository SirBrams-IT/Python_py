#While loop
#increment
number =20
while number <=25:
    number += 1
    print(number)

#decrement
count=105
while count>=100:
    count-=1
    print(count)

#For loop
for x in range(1,5):
    print(x)
for num in range(20,30,2):
    print(num)

#list
languages= ["Python", "kotlin","PHP"]
for lang in languages:
    print(lang)
