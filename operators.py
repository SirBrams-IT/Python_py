#Arithmetic Operators

num1=34
num2=10

print(num1+num2)
print(num1-num2)
print(num1*num2)
print(num1/num2)
print(num1 % num2) #modulus -Returns a remainder

#Comparison Operators
print(num1<num2)
print(num1>num2)
print(num1<=num2)
print(num1>=num2)
print(num1==num2) #Equal to
print(num1!=num2) #Not equal to

#Assignmet Operators
W = 7
print(W)

W+=3
print(W)

b=10
b-=2
print(b)

#Logic Operators -and, or, not
print(56< 100 and 45<23)
print(56< 100 or 45<23)
print(not 56< 100 and 45<23)
